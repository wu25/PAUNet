class spatial_attn_layer(nn.Module):
    def __init__(self, kernel_size=3):
        super(spatial_attn_layer, self).__init__()
        self.compress = ChannelPool()
        self.spatial = BasicConv(2, 1, kernel_size, stride=1, padding=(kernel_size-1) // 2, relu=False)
    def forward(self, x):
        # import pdb;pdb.set_trace()
        x_compress = self.compress(x)
        x_out = self.spatial(x_compress)
        scale = torch.sigmoid(x_out) # broadcasting
        return scale*x


class PAA(nn.Module):
    def __init__(self, qkv_bias=False, qk_scale=None, attn_drop=0., proj_drop=0., mode='fc'):
        super().__init__()
        self.compress=ChannelPool()
        self.tfc_1 = nn.Conv2d(2 ,1, (7,7), stride=1, padding=(7//2, 7//2), bias=True)
        self.theta_conv_1 = nn.Sequential(nn.Conv2d(in_channels=1,
                                                    out_channels=1, kernel_size=5, stride=1, padding=2, bias=False),
                                          nn.BatchNorm2d(1))

    def forward(self, x):
        B, C, H, W = x.shape

        pre=x
        x=self.compress(x)
        theta_h = self.theta_conv_1(x)#相位
        x_h = torch.cat([x* torch.cos(theta_h), x* torch.sin(theta_h)], dim=1)  #表示为电波
        x_1 = self.tfc_1(x_h)
        x_1=torch.sigmoid(x_1)
        return x_1*pre
class RPAA(nn.Module):
    def __init__(self, c, DW_Expand=2, FFN_Expand=2, drop_out_rate=0., act=nn.PReLU()):
        super().__init__()
        dw_channel = c * DW_Expand
        # Simplified Channel Attention
        self.body = nn.Sequential(
            nn.Conv2d(c, c, kernel_size=3, stride=1, padding=1, bias=False),
            act,
            nn.Conv2d(c, c, kernel_size=3, stride=1, padding=1, bias=False)
        )
        self.act = nn.LeakyReLU(0.2)
        self.ca=ca_layer(c)
        self.sca = nn.Sequential(
            nn.AdaptiveAvgPool2d(1),
            nn.Conv2d(in_channels=c, out_channels=c, kernel_size=1, padding=0, stride=1,
                      groups=1, bias=True),
        )
        self.paa=PAA()
        # self.sa=spatial_attn_layer()
        self.norm=LayerNorm2d(c)
        # self.conv1x1 = nn.Conv2d(c*2, c, kernel_size=1, bias=False)

        # self.fn= nn.Linear(c, c,bias=False)
    def forward(self, inp):
        x=inp
        x = self.body(x)

        x=self.norm(x)
        x = self.paa(x)
        x = x * self.sca(x)
        # x=self.ca(x)
        # x=self.fn(x)
        x=self.act(x)
        res = x+inp
        return res


class ca_layer(nn.Module):
    def __init__(self, channel, reduction=8, bias=True):
        super(ca_layer, self).__init__()
        # global average pooling: feature --> point
        self.avg_pool = nn.AdaptiveAvgPool2d(1)
        # feature channel downscale and upscale --> channel weight
        self.conv_du = nn.Sequential(
                nn.Conv2d(channel, channel // reduction, 1, padding=0, bias=bias),
                nn.ReLU(inplace=True),
                nn.Conv2d(channel // reduction, channel, 1, padding=0, bias=bias),
                nn.Sigmoid()
        )

    def forward(self, x):
        y = self.avg_pool(x)
        y = self.conv_du(y)
        return x * y

class FCBlock(nn.Module):
    def __init__(self, c, DW_Expand=2,  FFN_Expand=2, drop_out_rate=0., act=nn.PReLU()):
        super().__init__()
        self.sca = nn.Sequential(
            nn.AdaptiveAvgPool2d(1),
            nn.Conv2d(in_channels=c, out_channels=c, kernel_size=1, padding=0, stride=1,
                      groups=1, bias=True),
        )

        self.conv=nn.Conv2d(in_channels=c, out_channels=c//2, kernel_size=1, padding=0, stride=1, bias=True)

    def forward(self, inp):
        x=inp
        x=self.conv(x)
        return x

