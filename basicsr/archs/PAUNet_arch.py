class PAUNet(nn.Module):

    def __init__(self, img_channel=3, width=48, middle_blk_num=1, enc_blk_nums=[1,1,1], dec_blk_nums=[1,1,1]):
        super(PAUNet, self).__init__()

        self.intro = nn.Conv2d(in_channels=img_channel, out_channels=width, kernel_size=3, padding=1, stride=1,
                               groups=1,
                               bias=True)
        self.ending = nn.Conv2d(in_channels=width, out_channels=img_channel, kernel_size=3, padding=1, stride=1,
                                groups=1,
                                bias=True)

        self.encoders = nn.ModuleList()
        self.decoders = nn.ModuleList()
        self.middle_blks = nn.ModuleList()
        self.ups = nn.ModuleList()
        self.downs = nn.ModuleList()
        self.act = nn.LeakyReLU(0.2)
        self.fcs=nn.ModuleList()
        self.convs=nn.ModuleList()
        chan = width
        self.norms=nn.ModuleList()
        self.convs2=nn.ModuleList()
        for num in enc_blk_nums:
            self.encoders.append(
                nn.Sequential(
                    *[RPAA(chan) for _ in range(num)]
                )
            )
            self.downs.append(
                nn.Conv2d(chan, 2 * chan, 2, 2)
            )
            chan = chan * 2

        self.middle_blks = \
            nn.Sequential(
                *[RPAA(chan) for _ in range(middle_blk_num)]
            )

        for num in dec_blk_nums:
            self.ups.append(
                nn.Sequential(
                    nn.Conv2d(chan, chan * 2, 1, bias=False),
                    nn.PixelShuffle(2)
                )
            )

            chan = chan // 2
            self.fcs.append(
                FCBlock(chan * 2)
            )
            self.decoders.append(
                nn.Sequential(
                    *[RPAA(chan) for _ in range(num)]
                )
            )
        self.softmax = nn.Softmax(dim=1)
        self.padder_size = 2 ** len(self.encoders)

    def forward(self, inp):
        B, C, H, W = inp.shape


        inp = self.check_image_size(inp)

        x = self.intro(inp)

        encs = []
        for encoder, down in zip(self.encoders, self.downs):
            x = encoder(x)
            encs.append(x)
            x = down(x)
        x = self.middle_blks(x)

        for decoder,up,enc_skip,fc in zip(self.decoders,self.ups, encs[::-1],self.fcs):
            x=up(x)

            # x=torch.cat([x,enc_skip],dim=1)
            # x=fc(x)
            x=x+enc_skip
            x = decoder(x)
        x = self.ending(x)
        x = x + inp

        return x[:, :, :H, :W]

    def check_image_size(self, x):
        _, _, h, w = x.size()
        mod_pad_h = (self.padder_size - h % self.padder_size) % self.padder_size
        mod_pad_w = (self.padder_size - w % self.padder_size) % self.padder_size
        x = F.pad(x, (0, mod_pad_w, 0, mod_pad_h))
        return x


